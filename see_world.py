see_world = ['Moscwo', 'Minsk', 'Grodno', 'Krakow', 'Piter']
print(see_world)

# Alphabe order(see_world list)
print(sorted(see_world))

# Former form
print(see_world)

# Alphabetical order from the end
print(sorted(see_world, reverse=True))

